function changeLanguage(lang) {
    document.querySelectorAll('[data-lang]').forEach(element => {
        const key = element.getAttribute('data-text');
        if (texts[lang][key]) {
            if (element.tagName === 'IMG') {
                element.alt = texts[lang][key];
            } else {
                element.innerHTML = texts[lang][key];
            }
        }
        if (texts[lang][key]) {
            if (element.tagName === 'INPUT') {
                element.value = texts[lang][key]; // Для input меняем value
            } else {
                element.innerHTML = texts[lang][key]; // Для других элементов меняем текст
            }
        }


    });
}
document.getElementById('switchToRu').addEventListener('click', () => changeLanguage('ru'));
document.getElementById('switchToEn').addEventListener('click', () => changeLanguage('en'));
